
# Solicitar al usuario que ingrese su edad actual
edad_actual = int(input("Ingrese su edad actual: "))

# Calcular las edades futuras
edad_en_5_anios = edad_actual + 5
edad_en_10_anios = edad_actual + 10
edad_en_20_anios = edad_actual + 20

# Mostrar las edades futuras
print(f"\nDentro de 5 años, tendrás: {edad_en_5_anios} años")
print(f"Dentro de 10 años, tendrás: {edad_en_10_anios} años")
print(f"Dentro de 20 años, tendrás: {edad_en_20_anios} años")