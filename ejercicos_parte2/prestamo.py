# Datos del préstamo
monto_prestamo = 10000.00  # Monto del préstamo
tasa_interes_anual = 0.27   # Tasa de interés anual (27%)

# Cálculo del interés
interes = monto_prestamo * tasa_interes_anual

# Mostrar el resultado
print(f"El interés a pagar por un préstamo de {monto_prestamo} a una tasa del {tasa_interes_anual * 100}% es: {interes:.2f}")
